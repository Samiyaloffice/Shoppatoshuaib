package com.shoppa.Model;

public class OptionMenuModel {
    String mElementAnim, mElementTxt;

    public OptionMenuModel(String mElementAnim, String mElementTxt) {
        this.mElementAnim = mElementAnim;
        this.mElementTxt = mElementTxt;
    }

    public String getmElementAnim() {
        return mElementAnim;
    }

    public void setmElementAnim(String mElementAnim) {
        this.mElementAnim = mElementAnim;
    }

    public String getmElementTxt() {
        return mElementTxt;
    }

    public void setmElementTxt(String mElementTxt) {
        this.mElementTxt = mElementTxt;
    }
}
